import UIKit

var ad_offset_constant:CGFloat! = 0 // change this to zero if hiding ads, 50 if showing ads

extension UIColor {
    static var primary:UIColor {
        return UIColor(white: 0.1, alpha: 1)
    }
    static var primaryLight:UIColor {
        return UIColor(white: 0.105, alpha: 1)
    }
    static var secondary:UIColor {
        return UIColor(white: 0.125, alpha: 1)
    }
    static var highlight:UIColor {
        return UIColor(red:0.00, green:0.78, blue:0.89, alpha:1.0)
    }
}




